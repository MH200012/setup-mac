#!/usr/bin/env bash
#
# ==============================================================================
# Logging Library
# ==============================================================================
#
# Usage:
#
#   source scripts/common/log.sh
#
#   log_info "Installing Homebrew"
#   log_success "Completed"
#   log_warn "Already installed"
#   log_error "Installation failed"
#   log_step "Installing Python"
#
# ==============================================================================

set -Eeuo pipefail

################################################################################
# Configuration
################################################################################

LOG_DIR="${BOOTSTRAP_ROOT:-$(pwd)}/logs"
LOG_FILE="${LOG_DIR}/bootstrap.log"

mkdir -p "${LOG_DIR}"

################################################################################
# Colors
################################################################################

if [[ -t 1 ]]; then
    RED="\033[31m"
    GREEN="\033[32m"
    YELLOW="\033[33m"
    BLUE="\033[34m"
    MAGENTA="\033[35m"
    CYAN="\033[36m"
    BOLD="\033[1m"
    RESET="\033[0m"
else
    RED=""
    GREEN=""
    YELLOW=""
    BLUE=""
    MAGENTA=""
    CYAN=""
    BOLD=""
    RESET=""
fi

################################################################################
# Timestamp
################################################################################

timestamp() {
    date "+%Y-%m-%d %H:%M:%S"
}

################################################################################
# Internal Logger
################################################################################

_log() {

    local level="$1"
    local color="$2"
    shift 2

    local message="$*"

    local line
    line="$(timestamp) [${level}] ${message}"

    printf "%b%s%b\n" "${color}" "${line}" "${RESET}"

    printf "%s\n" "${line}" >> "${LOG_FILE}"

}

################################################################################
# Public API
################################################################################

log_info() {
    _log "INFO" "${BLUE}" "$@"
}

log_success() {
    _log "SUCCESS" "${GREEN}" "$@"
}

log_warn() {
    _log "WARN" "${YELLOW}" "$@"
}

log_error() {
    _log "ERROR" "${RED}" "$@"
}

log_debug() {

    if [[ "${DEBUG:-false}" == "true" ]]; then
        _log "DEBUG" "${MAGENTA}" "$@"
    fi

}

log_step() {

    echo
    printf "%b" "${BOLD}${CYAN}"
    printf "============================================================\n"
    printf " %s\n" "$*"
    printf "============================================================\n"
    printf "%b" "${RESET}"

    printf "%s [STEP] %s\n" "$(timestamp)" "$*" >> "${LOG_FILE}"

}

################################################################################
# Banner
################################################################################

log_banner() {

cat <<EOF

============================================================
                 Mac Bootstrap
============================================================

EOF

}

################################################################################
# Finish
################################################################################

log_finish() {

    echo
    log_success "Bootstrap completed successfully."

}

################################################################################
# Die
################################################################################

die() {

    log_error "$*"

    exit 1

}