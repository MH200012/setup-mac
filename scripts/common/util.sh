################################################################################
# Version
################################################################################

version() {

    "$1" --version | head -n1

}

################################################################################
# Wait
################################################################################

wait_spinner() {

    local pid="$1"

    local spin='-\|/'

    local i=0

    while kill -0 "${pid}" 2>/dev/null; do

        i=$(( (i+1) %4 ))

        printf "\r[%c] " "${spin:$i:1}"

        sleep 0.1

    done

    printf "\r"

}

###############################################################################
# Read repositories.toml
###############################################################################

readonly REPOSITORIES_CONFIG="${BOOTSTRAP_ROOT}/config/repositories.toml"

get_config() {
    local key="$1"

    python3 - "$REPOSITORIES_CONFIG" "$key" <<'PY'
import sys
import tomllib

config_file = sys.argv[1]
key = sys.argv[2]

with open(config_file, "rb") as f:
    data = tomllib.load(f)

print(data[key])
PY
}

