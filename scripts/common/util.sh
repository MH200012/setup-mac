#!/usr/bin/env bash
#
# ==============================================================================
# Utility Library
# ==============================================================================
#
# Shared utility functions used across all bootstrap scripts.
#
# ==============================================================================

set -Eeuo pipefail

################################################################################
# Constants
################################################################################

CPU_ARCH="$(uname -m)"
OS_NAME="$(uname -s)"

export CPU_ARCH
export OS_NAME

################################################################################
# Command Helpers
################################################################################

command_exists() {
    command -v "$1" >/dev/null 2>&1
}

require_command() {

    local cmd="$1"

    if ! command_exists "${cmd}"; then
        die "Required command not found: ${cmd}"
    fi

}

################################################################################
# OS Detection
################################################################################

is_macos() {
    [[ "${OS_NAME}" == "Darwin" ]]
}

check_macos() {

    if ! is_macos; then
        die "This bootstrap supports macOS only."
    fi

}

################################################################################
# CPU Detection
################################################################################

is_arm64() {
    [[ "${CPU_ARCH}" == "arm64" ]]
}

is_intel() {
    [[ "${CPU_ARCH}" == "x86_64" ]]
}

check_architecture() {

    if is_arm64; then
        log_info "Apple Silicon detected."
        return
    fi

    if is_intel; then
        log_info "Intel Mac detected."
        return
    fi

    die "Unsupported CPU architecture."
}

################################################################################
# Network
################################################################################

check_internet() {

    if curl -fsSL https://github.com >/dev/null 2>&1; then
        log_success "Internet connection OK."
        return
    fi

    die "Internet connection unavailable."

}

################################################################################
# Sudo
################################################################################

ensure_sudo() {

    sudo -v

    (
        while true; do
            sudo -n true >/dev/null 2>&1 || exit 0
            sleep 60
        done
    ) &
}

################################################################################
# Retry
################################################################################

retry() {

    local retries="${1}"
    shift

    local count=0

    until "$@"; do

        count=$((count + 1))

        if [[ ${count} -ge ${retries} ]]; then
            return 1
        fi

        log_warn "Retry ${count}/${retries}"

        sleep 2

    done

}

################################################################################
# Run Command
################################################################################

run() {

    log_debug "$*"

    "$@"

}

################################################################################
# Download
################################################################################

download() {

    local url="$1"
    local output="$2"

    curl \
        --fail \
        --location \
        --silent \
        --show-error \
        "${url}" \
        -o "${output}"

}

################################################################################
# Confirmation
################################################################################

confirm() {

    local prompt="${1:-Continue?}"

    read -r -p "${prompt} [y/N] " response

    case "${response}" in

        y|Y|yes|YES)
            return 0
            ;;

        *)
            return 1
            ;;

    esac

}

################################################################################
# Filesystem
################################################################################

mkdir_safe() {

    local dir="$1"

    mkdir -p "${dir}"

}

symlink_safe() {

    local src="$1"
    local dst="$2"

    if [[ -L "${dst}" ]]; then
        return
    fi

    ln -sf "${src}" "${dst}"

}

backup_file() {

    local file="$1"

    if [[ -f "${file}" ]]; then

        cp \
            "${file}" \
            "${file}.bak.$(date +%Y%m%d%H%M%S)"

    fi

}

